class InteDataSet(data.Dataset):
    def __init__(self,
                 image_dir,
                 anno_path,
                 input_transform=None,
                 labels_path=None):
        self.image_dir = image_dir
        self.anno_path = anno_path
        self.input_transform = input_transform
        self.labels_path = labels_path

        self.labels = []
        if os.path.exists(self.labels_path):
            self.labels = np.load(self.labels_path).astype(np.float64)
            self.labels = (self.labels > 0).astype(np.float64)  # multi-label binarization
        else:
            print('labels_path not exist, please check the path or run get_label_vector.py first')

        # Cache annotation data once at init — avoids re-reading JSON every __getitem__
        with open(self.anno_path, 'r') as f:
            self._annos_dict = json.load(f)
        self._annotations = self._annos_dict['annotations']
        # Pre-build image paths and caption lists
        self._image_paths = []
        self._captions = []
        for ann in self._annotations:
            img_path = os.path.join(self.image_dir, 'low', ann['image_id'] + '.jpg')
            self._image_paths.append(img_path)
            # psychological analysis text generated by the MLLM
            caption = ann.get('caption_by_Janus_7B', 'Nothing.')
            self._captions.append(caption)
        # Free the raw dict to save memory
        del self._annos_dict


    def _load_image(self, index):
        image_path = self._image_paths[index]
        caption = self._captions[index]
        if not os.path.exists(image_path):
            print(f"Warning: Image {image_path} does not exist.")
            return Image.new('RGB', (224, 224), (0, 0, 0)), caption

        return Image.open(image_path).convert("RGB"), caption

    def __getitem__(self, index):
        input, caption = self._load_image(index)
        if input is None:
            return None, None

        if self.input_transform:
            input = self.input_transform(input)

        label = self.labels[index]
        return input, label, caption

    def __len__(self):
        return self.labels.shape[0]

class METMEMEDataSet(data.Dataset):
    def __init__(self,
                 image_dir,
                 anno_path,
                 input_transform=None):
        self.image_dir = image_dir
        self.anno_path = anno_path
        self.input_transform = input_transform
        self.intent_categories = ['interactive', "expressive", "purely entertaining", "offensive", "other"]
        # cache annotations
        with open(self.anno_path, 'r') as f:
            self._annos_dict = json.load(f)
        self._annotations = self._annos_dict['annotations']

    def _load_image(self, index):
        image_path, caption = self._get_image_path(index)
        try:
            if not os.path.exists(image_path):
                print(f"Warning: Image {image_path} does not exist.")
                return Image.new('RGB', (224, 224), (0, 0, 0)), caption
            return Image.open(image_path).convert("RGB"), caption
        except (OSError, IOError) as e:
            print(f"Error loading image {image_path}: {e}")
            return Image.new('RGB', (224, 224), (0, 0, 0)), caption


    def _get_image_path(self, index):
        annos_i = self._annotations[index]
        img_id_i = annos_i['image_id']
        image_file_path = os.path.join(self.image_dir, img_id_i)

        # psychological analysis text generated by the MLLM (Janus-Pro-7B)
        caption = annos_i.get('caption_by_Janus_7B', '')
        if isinstance(caption, list):
            caption = " ".join(map(str, caption))
        elif not isinstance(caption, str):
            caption = str(caption)

        return image_file_path, caption


    def _get_category_vector(self, intent_category):
        vector = [0] * len(self.intent_categories)
        vector[intent_category] = 1
        return np.array(vector)

    def __getitem__(self, index):
        input, caption = self._load_image(index)
        if input is None:
            return None, None

        if self.input_transform:
            input = self.input_transform(input)

        annos_i = self._annotations[index]
        intent_category = annos_i['category_ids'][0]  # extract the intent category

        intent_vector = self._get_category_vector(intent_category)
        return input, intent_vector, caption

    def __len__(self):
        return len(self._annotations)

class MDIDDataSet(data.Dataset):
    def __init__(self,
                 image_dir,
                 anno_path,
                 ):
        self.image_dir = image_dir
        self.anno_path = anno_path

        # Intent labels for MDID
        self.intent_categories = [
            "provoke", "inform", "advocate", "entertain",
            "expose", "express", "promote"
        ]
        # cache annotations
        with open(self.anno_path, 'r') as f:
            self._annos_dict = json.load(f)
        self._annotations = self._annos_dict['annotations']

    def _load_image_features(self, index):
        # MDID images are not public; use pre-extracted ResNet-18 features (.npy)
        annos_i = self._annotations[index]
        img_id_i = annos_i['image_id']
        feature_file_path = os.path.join(self.image_dir, f"{img_id_i}.npy")

        if not os.path.exists(feature_file_path):
            print(f"Warning: Image feature {feature_file_path} does not exist.")
            return np.zeros((512,)), annos_i.get('caption', 'No caption available')

        image_features = np.load(feature_file_path)
        caption = annos_i.get('caption', 'No caption available')  # user-provided "Raw" caption

        return image_features, caption

    def _get_category_vector(self, intent_category):
        vector = [0] * len(self.intent_categories)
        vector[intent_category] = 1
        return np.array(vector)

    def __getitem__(self, index):
        image_features, caption = self._load_image_features(index)

        annos_i = self._annotations[index]
        if 'category_ids' in annos_i:
            intent_category = annos_i['category_ids'][0]
        elif "category_ids_softprob" in annos_i:
            intent_category = annos_i['category_ids_softprob'][0]
            intent_category = intent_category.index(1)

        intent_vector = self._get_category_vector(intent_category)

        return image_features, intent_vector, caption

    def __len__(self):
        return len(self._annotations)


class TwoCropTransform:
    # https://github.com/HobbitLong/SupContrast/blob/331aab5921c1def3395918c05b214320bef54815/util.py#L9
    """Create two augmentations of the same image"""
    def __init__(self, transform1, transform2):
        self.transform1 = transform1
        self.transform2 = transform2

    def __call__(self, x):
        return [self.transform1(x), self.transform2(x)]


def get_datasets(args):
    trivialaugment = aug_lib.TrivialAugment()
    if args.orid_norm:
        normalize = transforms.Normalize(mean=[0, 0, 0],
                                         std=[1, 1, 1])
    else:
        normalize = transforms.Normalize(mean=[0.485, 0.456, 0.406],
                                     std=[0.229, 0.224, 0.225])


    transform_list1 = [
        transforms.Resize((args.img_size_hight, args.img_size_weight)),
        SLCutoutPIL(n_holes=args.n_holes, length=args.length),
        RandAugment(),
        transforms.ToTensor(),
        normalize
    ]
    transform_list = transforms.Compose(transform_list1)

    test_data_transform = transforms.Compose([
                                            transforms.Resize((args.img_size_hight, args.img_size_weight)),
                                            transforms.ToTensor(),
                                            normalize])

    if args.dataname == 'intentonomy':
        train_dataset = InteDataSet(
            image_dir=inte_image_path,
            anno_path=inte_train_anno_path,
            input_transform=TwoCropTransform(transform_list, transform_list),
            labels_path=os.path.join(inte_image_path, 'train_label_vectors_intentonomy2020.npy'),
        )
        val_dataset = InteDataSet(
            image_dir=inte_image_path,
            anno_path=inte_val_anno_path,
            input_transform=test_data_transform,
            labels_path=os.path.join(inte_image_path, 'val_label_vectors_intentonomy2020.npy'),
        )
        test_dataset = InteDataSet(
            image_dir=inte_image_path,
            anno_path=inte_test_anno_path,
            input_transform=test_data_transform,
            labels_path=os.path.join(inte_image_path, 'test_label_vectors_intentonomy2020.npy'),
        )
    elif args.dataname == 'METMEME':
        train_dataset = METMEMEDataSet(
            image_dir=METME_image_path,
            anno_path=METME_train_anno_path,
            input_transform=TwoCropTransform(transform_list, transform_list),
        )
        val_dataset = METMEMEDataSet(
            image_dir=METME_image_path,
            anno_path=METME_val_anno_path,
            input_transform=test_data_transform,
        )
        test_dataset = METMEMEDataSet(
            image_dir=METME_image_path,
            anno_path=METME_test_anno_path,
            input_transform=test_data_transform,
        )

    elif args.dataname == 'MDID':
        train_dataset = MDIDDataSet(
            image_dir=mdid_image_path,
            anno_path=mdid_train_anno_path,
        )
        val_dataset = MDIDDataSet(
            image_dir=mdid_image_path,
            anno_path=mdid_val_anno_path,
        )
        test_dataset = MDIDDataSet(
            image_dir=mdid_image_path,
            anno_path=mdid_test_anno_path,
        )
    else:
        raise NotImplementedError("Unknown dataname %s" % args.dataname)


    print("len(train_dataset):", len(train_dataset))
    print("len(val_dataset):", len(val_dataset))
    print("len(test_dataset):", len(test_dataset))

    return train_dataset, val_dataset, test_dataset
